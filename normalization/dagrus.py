"""
DagrusNormalizer — нормализатор текста для датасета DagRus.

Датасет содержит специфическую разметку:
    1. #маркер#        — технические события → удаляются
                         (#говорит на другом языке#, #смех#, #перечисляет имена#, ...)
    2. слово{канон*}   — произносимая форма + канонический вариант → берётся канон
                         (щас{сейчас*} → сейчас, ээ{э*} → э)
    3. слово{форма%}   — нестандартная форма без строгого канона → аннотация удаляется
    4. нрзб            — неразборчиво (standalone) → удаляется
    5. UNMARKED_EVENTS — технические фразы без скобок (забыли добавить #) → удаляются
                         ("говорит на другом языке", ...)

Ключевая идея для пар щас/сейчас:
    - normalize_ref("щас{сейчас*}") → "сейчас"   (канон из аннотации)
    - normalize_hyp("щас")          → "сейчас"   (через COLLOQUIAL_MAP)
    - normalize_hyp("сейчас")       → "сейчас"   (уже канон, без изменений)
    Итог: оба варианта ASR считаются верными.
"""

import re
from typing import Tuple
from .base import BaseNormalizer


# ===========================================================================
# Разговорные → канонические формы
# Извлечено из аннотаций датасета + дополнено вручную.
#
# ВАЖНО: включаем только однозначные замены, где слово не несёт
# самостоятельного смысла в нормальной речи.
# Не включаем: "не" → "нет" (омоним!), "та" → "да" (омоним!)
# ===========================================================================
COLLOQUIAL_MAP: dict[str, str] = {
    # Удлинённые гласные / согласные → одиночная форма
    "ээ":           "э",
    "аа":           "а",
    "уу":           "у",
    "мм":           "м",
    "нуу":          "ну",
    "даа":          "да",
    "воот":         "вот",
    "неет":         "нет",

    # Орфографические / диалектные варианты
    # Ключи пишем через «е» (ё → е применяется раньше в _finalize)
    "щас":          "сейчас",
    "че":           "что",     # чё → че → что
    "када":         "когда",
    "тада":         "тогда",
    "грит":         "говорит",
    "горит":        "говорит",
    "горят":        "говорят",
    "тыща":         "тысяча",
    "скоко":        "сколько",
    "скока":        "сколько",
    "ихний":        "их",
    "восемьсят":    "восемьдесят",
    "пиисят":       "пятьдесят",
    "шиисят":       "шестьдесят",
    "семьсят":      "семьдесят",
    "ваще":         "вообще",
    "токо":         "только",
    "тока":         "только",

    # Неразборчиво → удалить
    "нрзб":         "",
    "нрзб.":        "",
}

# ===========================================================================
# Дискурсивные маркеры / филлеры
# Если remove_fillers=True — удаляются из обоих текстов.
# По умолчанию НЕ удаляются: пропуск моделью — это зафиксированная ошибка.
# ===========================================================================
DISCOURSE_FILLERS: set[str] = {
    "угу", "ага", "мгм", "э", "м",
}

# ===========================================================================
# Технические фразы без #-маркеров (пропуск разметчика).
# Применяются к ref (без учёта регистра, точное вхождение подстроки).
# ===========================================================================
UNMARKED_EVENTS: tuple = (
    "говорит на другом языке",
    "говорят на другом языке",
    "перечисляет имена",
    "перечисляет числа",
    "смеется",
    "смеётся",
    "смеются",
)

# ===========================================================================
# Регулярные выражения
# ===========================================================================
_MARKUP_RE     = re.compile(r'#[^#]+#')
_ANNOTATION_RE = re.compile(r'(\S*)\{([^}]+)[*%]\}')
_NRZB_RE       = re.compile(r'\bнрзб\.?\b', re.IGNORECASE)
_SPACES_RE     = re.compile(r'\s+')
_PUNCT_RE      = re.compile(r'[^\wа-яa-z\d\s]', re.UNICODE)   # ё уже заменена → не нужна

# Паттерн для UNMARKED_EVENTS строится один раз при загрузке модуля
_UNMARKED_RE   = re.compile(
    '|'.join(re.escape(p) for p in UNMARKED_EVENTS),
    re.IGNORECASE,
)


class DagrusNormalizer(BaseNormalizer):
    """
    Нормализатор текста, специфичный для датасета DagRus.

    Применяется как к референсу (с разбором аннотаций),
    так и к гипотезе ASR (только COLLOQUIAL_MAP).

    Пример:
        norm = DagrusNormalizer()
        ref, hyp = norm.normalize_pair(ref_raw, hyp_raw)
    """

    def __init__(
        self,
        remove_fillers: bool = False,
        lowercase: bool = True,
        strip_punctuation: bool = True,
    ):
        """
        Args:
            remove_fillers:    Удалять дискурсивные маркеры (угу, ага, э, м, ...).
                               False по умолчанию — пропуск моделью фиксируется как ошибка.
            lowercase:         Приводить к нижнему регистру.
            strip_punctuation: Удалять знаки препинания.
        """
        self.remove_fillers    = remove_fillers
        self.lowercase         = lowercase
        self.strip_punctuation = strip_punctuation

    # ------------------------------------------------------------------
    # BaseNormalizer API
    # ------------------------------------------------------------------

    def normalize_ref(self, text: str) -> str:
        """
        Нормализует референсный текст с аннотациями DagRus.

        Шаги:
            1. Удаляем #маркеры# (смех, другой язык и т.д.)
            2. Раскрываем слово{канон*} → канон
            3. Удаляем standalone нрзб
            4. Применяем общую нормализацию
        """
        if not text:
            return ""

        text = _MARKUP_RE.sub(' ', text)             # #маркер# → убрать
        text = _UNMARKED_RE.sub(' ', text)           # маркер без # → убрать
        text = _ANNOTATION_RE.sub(self._replace_annotation, text)
        text = _NRZB_RE.sub(' ', text)

        return self._finalize(text)

    def normalize_hyp(self, text: str) -> str:
        """
        Нормализует гипотезу ASR-модели.

        Аннотаций нет — только COLLOQUIAL_MAP и общая нормализация.
        Благодаря маппингу щас→сейчас, ээ→э и т.д. обе формы
        (произносимая и каноническая) считаются верными.
        """
        if not text:
            return ""
        return self._finalize(text, apply_colloquial=True)

    # ------------------------------------------------------------------
    # Private
    # ------------------------------------------------------------------

    @staticmethod
    def _replace_annotation(m: re.Match) -> str:
        """слово{канон*} → канон (или '' если канон = нрзб)."""
        canonical = m.group(2).strip()
        if not canonical or canonical.lower().startswith('нрзб'):
            return ' '
        return canonical

    def _finalize(self, text: str, apply_colloquial: bool = False) -> str:
        if self.lowercase:
            text = text.lower()

        # ё и е считаем одинаковыми: большинство моделей пишут «е»
        text = text.replace('ё', 'е')

        if apply_colloquial:
            words = text.split()
            result = []
            for w in words:
                mapped = COLLOQUIAL_MAP.get(w)
                if mapped is None:
                    result.append(w)
                elif mapped:        # непустая замена
                    result.append(mapped)
                # mapped == "" → слово удаляется (нрзб и т.п.)
            text = ' '.join(result)

        if self.strip_punctuation:
            text = _PUNCT_RE.sub(' ', text)

        if self.remove_fillers:
            words = text.split()
            text = ' '.join(w for w in words if w not in DISCOURSE_FILLERS)

        return _SPACES_RE.sub(' ', text).strip()
